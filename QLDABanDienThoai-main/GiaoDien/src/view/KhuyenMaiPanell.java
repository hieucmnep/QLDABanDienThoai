    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        String maKM = txtTimTheoma.getText();
        boolean searchResult = searchByMaKhuyenMai(maKM);

        if (searchResult) {
            JOptionPane.showMessageDialog(this, "Tìm mã khuyến mãi thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy mã khuyến mãi");
        }
    }

    private boolean searchByMaKhuyenMai(String maKM) {
        KhuyenMaii khuyenMai = khuyenMaiService.timKhuyenMaiTheoMa(maKM);

        DefaultTableModel model = (DefaultTableModel) tblKM.getModel();
        model.setRowCount(0);

        if (khuyenMai != null) {
            Object[] row = {khuyenMai.getMaKM(), khuyenMai.getPhanTramGiamGia(), khuyenMai.getSoLuong(), khuyenMai.getNgayBatDau(), khuyenMai.getNgayKetThuc()};
            model.addRow(row);
            return true; // Search successful
        } else {
            return false; // Search unsuccessful
        }
    }//GEN-LAST:event_btnTimActionPerformed

    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
       // Lấy thông tin từ các trường nhập liệu
    String phanTramGiam = txtphantramgiam.getText();
    String soLuong = txtsoluong.getText();
    Date ngayBatDau = ngaybatdau.getDate();
    Date ngayKetThuc = ngayketthuc.getDate();

    // Validate dữ liệu trước khi thêm
    if (!validateInput(phanTramGiam, soLuong, ngayBatDau, ngayKetThuc)) {
        return;
    }

    // Thêm mã khuyến mãi mới
    khuyenMaiService.themKhuyenMai(phanTramGiam, Integer.parseInt(soLuong), ngayBatDau, ngayKetThuc);

    // Load lại dữ liệu vào bảng
    loadDataToTable();

    // Thông báo thành công
    JOptionPane.showMessageDialog(this, "Thêm mã khuyến mãi thành công");
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        int selectedRow = tblKM.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hàng để sửa");
            return;
        }

        // Lấy thông tin từ hàng đã chọn
        String maKM = tblKM.getValueAt(selectedRow, 0).toString();
        String phanTramGiamGia = txtphantramgiam.getText();
        int soLuong = Integer.parseInt(txtsoluong.getText());
        Date ngayBatDau = ngaybatdau.getDate();
        Date ngayKetThuc = ngayketthuc.getDate();

        // Cập nhật thông tin khuyến mãi
        khuyenMaiService.suaKhuyenMai(maKM, phanTramGiamGia, soLuong, ngayBatDau, ngayKetThuc);

        // Load lại dữ liệu vào bảng
        loadDataToTable();

        // Thông báo thành công
        JOptionPane.showMessageDialog(this, "Sửa thông tin khuyến mãi thành công");
    }//GEN-LAST:event_btnsuaActionPerformed

    private void tblKMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKMMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblKM.getSelectedRow();

        if (selectedRow != -1) {
            // Lấy thông tin từ hàng đã chọn
            String maKM = tblKM.getValueAt(selectedRow, 0).toString();
            String phanTramGiamGia = tblKM.getValueAt(selectedRow, 1).toString();
            String soLuong = tblKM.getValueAt(selectedRow, 2).toString();
            Date ngayBatDau = (Date) tblKM.getValueAt(selectedRow, 3);
            Date ngayKetThuc = (Date) tblKM.getValueAt(selectedRow, 4);

            // Hiển thị thông tin trong các trường trên giao diện
            txtphantramgiam.setText(phanTramGiamGia);
            txtsoluong.setText(soLuong);
            ngaybatdau.setDate(ngayBatDau);
            ngayketthuc.setDate(ngayKetThuc);
        }
    }//GEN-LAST:event_tblKMMouseClicked

