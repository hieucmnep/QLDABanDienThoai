    private void loadDataToTable() {
        List<KhuyenMaii> danhSachKhuyenMai = khuyenMaiService.layTatCaKhuyenMai();

        DefaultTableModel model = (DefaultTableModel) tblKM.getModel();
        model.setRowCount(0);

        for (KhuyenMaii km : danhSachKhuyenMai) {
            Object[] row = {km.getMaKM(), km.getPhanTramGiamGia(), km.getSoLuong(), km.getNgayBatDau(), km.getNgayKetThuc()};
            model.addRow(row);
        }
    }

    private boolean validateInput(String phanTramGiam, String soLuong, Date ngayBatDau, Date ngayKetThuc) {
        if (phanTramGiam.isEmpty() || soLuong.isEmpty() || ngayBatDau == null || ngayKetThuc == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin");
            return false;
        }

        try {
            int phanTram = Integer.parseInt(phanTramGiam);
            int soLuongKM = Integer.parseInt(soLuong);

            if (phanTram < 1 || phanTram > 100) {
                JOptionPane.showMessageDialog(this, "Phần trăm giảm giá phải nằm trong khoảng từ 1 đến 100");
                return false;
            }

            if (soLuongKM < 0) {
                JOptionPane.showMessageDialog(this, "Số lượng không được âm");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Phần trăm giảm và số lượng phải là số nguyên");
            return false;
        }

        if (ngayBatDau.after(ngayKetThuc)) {
            JOptionPane.showMessageDialog(this, "Ngày kết thúc phải sau ngày bắt đầu");
            return false;
        }

        return true;
    }

